    // validacion de los datos a la hora del registro
    public boolean comprobacionCorrecto() {
        //copiamos la varibale de arriba, en caso de que el contenido se encuentre vacio nos lo especificara medainte un JOptionPane
        String name = txt_nombreuser.getText();
        String apellido = txt_apellidosuser.getText();
        String pass = txt_passuser.getText();
        String email = txt_emailuser.getText();
        String contact = txt_telefonouser.getText();
        //Selecionamos las celadas en caso de que queden vacias, se indicara un error 
        if (name.equals("")) {
            JOptionPane.showMessageDialog(this, "El contenido del nombre no esta relleno \n porfavor introduzca su nombre");
            return false;
        }
        if (apellido.equals("")) {
            JOptionPane.showMessageDialog(this, "El contenido del apellido se encuentra vacio \n porfavor introduzca su apellido");
            return false;
        }
        if (pass.equals("")) {
            JOptionPane.showMessageDialog(this, "El contenido del contraseña no esta relleno \n porfavor introduzca su contraseña - password");
            return false;
        }
        if (email.equals("") || !email.matches("^.+@.+\\..+$")) {
            JOptionPane.showMessageDialog(this, "El contenido del email no esta relleno \n o no corresponde con un correo, debe tener una terminacion en @gmail.com, @hotmail.es etc");
            return false;
        }
        if (contact.equals("")) {
            JOptionPane.showMessageDialog(this, "El contenido del contacto no esta relleno \n porfavor introduzca su numero de contacto");
            return false;
        }

        return true;
    }

