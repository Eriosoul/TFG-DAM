    private void bGuardarUserActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bGuardarUserActionPerformed
        // TODO add your handling code here:
        // realizamos una insercion por llamada de metodos 
        Modelo.SQLConsultas modSql = new SQLConsultas();
        Modelo.MetodoUsuario modUser = new MetodoUsuario();
        //si nuestro metodo insercion de datos esta correcto procedemos a comprobar las contraseñas
        if (comprobacionCorrecto() == true) {
            // comprobamos el contendio de las 2 contraseñas
            String pass = new String(txt_passuser.getPassword());
            String passConfirmar = new String(txt_passConfrimacion.getPassword());
            // si son iguales continua
            if (pass.equals(passConfirmar)) {
                // comprobamos de que no se repitan los correos, entendemos que los nombres si se pueden repetir
                if (modSql.existeUser(txt_emailuser.getText()) == 0) {
                    // creamos una variable donde le pasaremos la contraseña almacenada
                    //String nuevaContra = CifradoHash.sha1(pass); 
                    String nuevaContra = pass;

                    System.out.println(nuevaContra);
                    modUser.setNombre(txt_nombreuser.getText());
                    modUser.setApellidos(txt_apellidosuser.getText());
                    modUser.setEmail(txt_emailuser.getText());
                    modUser.setPass(nuevaContra);
                    modUser.setTelefono(txt_telefonouser.getText());
                    modUser.setDireccioncasa(txt_direccionuser.getText());
                    // le indicamos que el usuario sera tipo 2 osea cliente
                    modUser.setTipo(2);
                    // si el metodo registrar usuario esta todo correcto se procedera a limirar el formulario y ya dispondremos de nuetsra cuenta
                    if (modSql.registrarUser(modUser)) {
                        JOptionPane.showMessageDialog(null, "Usuario creado");
                        limpiarForm();
                    } else {
                        JOptionPane.showMessageDialog(null, "No se ha podido guardar");
                    }
                } else {
                    JOptionPane.showMessageDialog(null, "Ese correo ya esta en uso");
                }
            } else {
                JOptionPane.showMessageDialog(null, "Las contraseñas no coinciden");
            }
        } else {
            System.out.println("datos no introducidos");
        }
    }//GEN-LAST:event_bGuardarUserActionPerformed

