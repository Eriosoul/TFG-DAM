    private void ComboCompradoItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_ComboCompradoItemStateChanged
        // TODO add your handling code here:
        String seleciontusuario = (String) ComboComprado.getSelectedItem();//igualamos los items de nuestro combo a una variable string
        Connection con = ConexionDB.Conexion.getConnection();
        PreparedStatement ps;
        ResultSet rs;
        //realizamos la consulta de nuestra tabla venta donde combo selecionado
        String sql = "select id from prueba1 where fecha='" + seleciontusuario + "';";
        try {
            //realizamos la consulta
            ps = con.prepareStatement(sql);
            //ejecutamos la consulta
            rs = ps.executeQuery();
            if (rs.next()) {
                // y devolvemos el id selecionado
                comprarealizada = rs.getInt("id");
            }
        } catch (Exception e) {
            System.out.println(e.getMessage());
        }
        //JOptionPane.showMessageDialog(null, "El id del combo principal es " + comprarealizada);
    }//GEN-LAST:event_ComboCompradoItemStateChanged

    private void ComboCompradoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ComboCompradoActionPerformed
        // TODO add your handling code here:
        int totaldeFactura = 0;
        // cuando realizamos la acion del clicar en nuestro combo box  veremos si hay item selecionado 
        if (ComboComprado.getSelectedIndex() > 0) {
            // en caso de que haya selecionado pasara a realizar lo que queremos

            //creamos un metodo y se lo pasamos a nuestra tabla
            DefaultTableModel modelo = new DefaultTableModel();
            jTablaMiFactura.setModel(modelo);
            //realizamos la consulta de nuestras 2 tablas, en el cual selecionamos el id -- donde nuestro combo box estuvo selecionado
            String sql = "SELECT id, total, fecha, nombre, precio,cantidad, etadoactual\n" +
                    " FROM prueba1 C, prueba2 O, tipo_estado E WHERE (C.id = O.id_prueba1 AND O.estado = E.id_estado) and C.id=" + comprarealizada;
            try {
                //procedemos con la conexion a nuestra consulta
                ps = con.prepareStatement(sql);
                // procedemos a realizar la consulta
                System.out.println(ps);
                rs = ps.executeQuery();

                //result set para la tabla
                // aqui lo que hacemos es pasarle el resultado de la consulta
                ResultSetMetaData rsMd = rs.getMetaData();
                int cantidadColumnas = rsMd.getColumnCount();
                // creamos columnas
                modelo.addColumn("Nombre");
                modelo.addColumn("Precio");
                modelo.addColumn("Cantidad");
                modelo.addColumn("Estado");
                while (rs.next()) {
                    txtidFactura = rs.getInt("id");
                    totaldeFactura = rs.getInt("total");
                    Object filas[] = new Object[7];
                    filas[0] = rs.getObject(4);
                    filas[1] = rs.getObject(5);
                    filas[2] = rs.getObject(6);
                    filas[3] = rs.getObject(7);
                    modelo.addRow(filas);
                }

            } catch (Exception e) {
            }
            txt_idFactura.setEditable(false);
            txt_idFactura.setText(String.valueOf(txtidFactura));
            txtTotalFactura.setEditable(false);
            txtTotalFactura.setText(String.valueOf(totaldeFactura));
            System.out.println(totaldeFactura);
        } else {
            jTablaMiFactura.removeAll();
        }
    }//GEN-LAST:event_ComboCompradoActionPerformed

