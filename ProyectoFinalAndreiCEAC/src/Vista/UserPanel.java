    public boolean updateUserAcount() {
        Boolean isUpdated = false;
        //creamos unas variables para conseguir los datos y visualizarlos
        String unombre = txt_updnombre.getText();
        String uapellido = txt_updapellidos.getText();
        String upass = new String(txt_updpass.getPassword());
        String upss2 = new String(txt_updpass2.getPassword());
        String utelefono = txt_updtelefono.getText();
        String udireccion = txt_upddireccion.getText();
        //creamos una variable tipo texto y lo igualamos a "lo que contiene el if"

        try {
            if (upass.equals(upss2)) { //comparamos las contraseñas 
                Connection con = ConexionDB.Conexion.getConnection();
                // creamos una variable llamada nuevaContra y lo igualamos a las contraseña ya que si se cumple la condicion se almaena en la nueva variable

                //String nuevaContra = CifradoHash.sha1(upass);
                String nuevaContra = upass;
                String sql = "update user set nombre= ?,apellidos= ?,pass=?, telefono= ?,direccioncasa= ? where id_user=" + idUserPanel;
                System.out.println(sql);
                PreparedStatement pst = con.prepareStatement(sql);
                pst.setString(1, unombre);
                pst.setString(2, uapellido);
                pst.setString(3, nuevaContra);
                pst.setString(4, utelefono);
                pst.setString(5, udireccion);
                int rowCount = pst.executeUpdate();

                if (rowCount > 0) {
                    isUpdated = true;
                } else {
                    isUpdated = false;
                }

            } else {
                JOptionPane.showMessageDialog(null, "Las contraseñas no coinciden");
            }

        } catch (Exception e) {
            System.out.println(e.getMessage());
        }
        return isUpdated;
    }

